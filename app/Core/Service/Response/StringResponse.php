<?php


namespace App\Core\Service\Response;


class StringResponse extends BasicResponse
{
    public string $result;

    /**
     * @return string
     */
    public function getResult(): string
    {
        return $this->result;
    }

    /**
     * @param string $result
     */
    public function setResult(string $result): void
    {
        $this->result = $result;
    }
}
